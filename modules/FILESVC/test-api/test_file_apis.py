# ─── Entry point ────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="File Service API test suite")
    parser.add_argument("--base-url", default=BASE_URL, help="Base API URL")
    parser.add_argument("--report", default="file_api_test_report.html", help="Output HTML file")
    parser.add_argument("--problems-report", default="file_problems_report.md", help="Output problems .md file")
    args = parser.parse_args()

    client = APIClient(args.base_url)
    start_time = time.perf_counter()

    print("=" * 60)
    print("  File Service API Test Suite")
    print(f"  Base URL: {args.base_url}")
    print("=" * 60)

    auth_suite, token = login(client, ADMIN_USERNAME, ADMIN_PASSWORD, "Auth")
    second_token = None
    if SECOND_USER_USERNAME and SECOND_USER_PASSWORD:
        second_auth_suite, second_token = login(client, SECOND_USER_USERNAME, SECOND_USER_PASSWORD, "Auth (Second User)")
    else:
        second_auth_suite = TestSuite("Auth (Second User)")  # empty — not configured

    observations: list = []
    created_ids: dict[str, list[int]] = {"FileDocument": []}
    file_suite = TestSuite("FileDocument")

    try:
        file_suite, observations = test_file_document(client, token, second_token, created_ids)
    finally:
        # Always runs, even if a test above raised partway through — otherwise a
        # single failure would silently skip teardown and leak test data.
        cleanup(client, token, created_ids)

    duration = time.perf_counter() - start_time
    suites = [auth_suite, second_auth_suite, file_suite]

    print("\n" + "=" * 60)
    print("  SUMMARY")
    print("=" * 60)
    total_pass = sum(s.passed for s in suites)
    total_all = sum(s.total for s in suites)
    for s in suites:
        if s.total == 0:
            continue
        bar = "█" * s.passed + "░" * s.failed
        print(f"  {s.name:<24} {s.passed:>3}/{s.total:<3}  {bar}")
    print(f"\n  Total: {total_pass}/{total_all} passed  ({duration:.1f}s)")
    if observations:
        print(f"  Exploratory observations: {len(observations)} (see HTML report — not pass/fail)")

    generate_html_report(suites, observations, args.report, duration, args.base_url)
    problem_count = generate_problems_report(suites, args.problems_report)
    if problem_count:
        print(f"  ⚠ {problem_count} item(s) in problems report — review before considering this a clean run.")

    sys.exit(0 if total_pass == total_all else 1)


if __name__ == "__main__":
    main()
