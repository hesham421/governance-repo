# ─── HTTP helper (fixed) ───────────────────────────────────────────────────────

class APIClient:
    def __init__(self, base_url: str):
        self.base_url = base_url.rstrip("/")
        self.session = requests.Session()
        self.session.headers.update({
            "Accept": "application/json",
            "Content-Type": "application/json",
        })

    def _make(self, method: str, path: str, token: Optional[str] = None,
              expected: Optional[list[int]] = None, headers: Optional[dict] = None,
              **kwargs) -> TestResult:
        url = f"{self.base_url}/{path.lstrip('/')}"
        headers = dict(headers) if headers else {}
        if token:
            headers["Authorization"] = f"Bearer {token}"
        if expected is None:
            expected = [200]

        start = time.perf_counter()
        try:
            resp = self.session.request(method.upper(), url, headers=headers, timeout=TIMEOUT, **kwargs)
            duration = (time.perf_counter() - start) * 1000
            content_type = resp.headers.get("Content-Type", "")
            if "application/json" in content_type:
                try:
                    body = json.dumps(resp.json(), indent=2, ensure_ascii=False)
                except Exception:
                    body = resp.text[:500]
            else:
                # Binary (download) or non-JSON response — don't attempt to decode as JSON.
                body = f"<{len(resp.content)} bytes, Content-Type: {content_type}>"
            return TestResult(
                name="", method=method.upper(), url=url, status_code=resp.status_code,
                passed=resp.status_code in expected, expected_statuses=expected,
                duration_ms=round(duration, 1), response_body=body,
            )
        except requests.exceptions.ConnectionError:
            duration = (time.perf_counter() - start) * 1000
            return TestResult(
                name="", method=method.upper(), url=url, status_code=None, passed=False,
                expected_statuses=expected, duration_ms=round(duration, 1),
                error="Connection refused — is the server running?",
            )
        except Exception as exc:
            duration = (time.perf_counter() - start) * 1000
            return TestResult(
                name="", method=method.upper(), url=url, status_code=None, passed=False,
                expected_statuses=expected, duration_ms=round(duration, 1), error=str(exc),
            )

    def get(self, path, token=None, expected=None, **kw):
        return self._make("GET", path, token, expected, **kw)

    def post(self, path, token=None, expected=None, **kw):
        return self._make("POST", path, token, expected, **kw)

    def delete(self, path, token=None, expected=None, **kw):
        return self._make("DELETE", path, token, expected, **kw)

    def post_multipart(self, path, token=None, expected=None, files=None, **kw):
        """
        Multipart upload (API-FILE-002). Content-Type must NOT be the session's
        default 'application/json' — setting it to None here unsets it for this
        call only, letting `requests` compute the correct multipart boundary header.
        """
        return self._make("POST", path, token, expected,
                           headers={"Content-Type": None}, files=files, **kw)


# ─── Test runner helpers (fixed) ───────────────────────────────────────────────

def run(suite: TestSuite, name: str, result: TestResult, note: str = "") -> TestResult:
    result.name = name
    result.note = note
    suite.results.append(result)
    status = "✓" if result.passed else "✗"
    code = result.status_code or "ERR"
    print(f"  {status} [{code}] {name} ({result.duration_ms:.0f}ms)")
    if not result.passed and result.error:
        print(f"      Error: {result.error}")
    return result


def run_observation(bucket: list, name: str, source: str, result: TestResult) -> None:
    obs = Observation(
        name=name, method=result.method, url=result.url, status_code=result.status_code,
        duration_ms=result.duration_ms, source=source, response_body=result.response_body,
    )
    bucket.append(obs)
    code = result.status_code or "ERR"
    print(f"  👁 [{code}] {name} — observed (source: {source})")


def data_of(result: TestResult):
    """
    Envelope shape {success, message, data, error, timestamp, correlationId} per
    index.md's Common Response Envelope (schema: ApiResponseFileUploadResponse).
    """
    if not result.response_body or result.response_body.startswith("<"):
        return None
    try:
        return json.loads(result.response_body).get("data")
    except Exception:
        return None


def error_code_of(result: TestResult) -> Optional[str]:
    """
    Runtime error.code value (underscored, e.g. FILE_SIZE_EXCEEDED) per index.md's
    Known Error Codes table — NEVER the governance ERR-FILE-NNNN hyphenated ID.
    """
    if not result.response_body or result.response_body.startswith("<"):
        return None
    try:
        body = json.loads(result.response_body)
        err = body.get("error")
        return err.get("code") if isinstance(err, dict) else None
    except Exception:
        return None


def get_field(result: TestResult, key: str):
    d = data_of(result)
    return d.get(key) if isinstance(d, dict) else None


def extract_token(auth_result: TestResult) -> Optional[str]:
    d = data_of(auth_result)
    return d.get("accessToken") if isinstance(d, dict) else None


def make_bytes(n: int) -> bytes:
    """Deterministic filler content of exactly n bytes — not a real document, just sized data."""
    pattern = b"FILE-API-TEST-DATA-"
    reps = (n // len(pattern)) + 1
    return (pattern * reps)[:n]


def tamper_token(token: str) -> str:
    """
    Flip one character of an opaque encrypted token to simulate corruption
    (RULE-FILE-003 / ERR-FILE-0003, tampered case). Deterministic, not random,
    so failures are reproducible.
    """
    if not token:
        return token
    idx = len(token) // 2
    chars = list(token)
    alt = "x" if chars[idx] != "x" else "y"
    # Only substitute if it keeps the token the same length / charset-safe;
    # encryptedToken is URL-safe per the docs (used as a path segment).
    chars[idx] = alt
    return "".join(chars)


