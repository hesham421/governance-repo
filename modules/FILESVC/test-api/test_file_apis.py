def issue_upload_token(client, token, category_fk, expected, owner_id=OWNER_ID,
                        owner_type=OWNER_TYPE, module_code=MODULE_CODE):
    return client.post(
        "api/v1/files/upload-token", token=token, expected=expected,
        json={"ownerId": owner_id, "ownerType": owner_type,
              "moduleCode": module_code, "fileCategoryFk": category_fk},
    )


def issue_access_token(client, token, file_document_pk, action, expected):
    return client.post(
        f"api/v1/files/{file_document_pk}/access-token", token=token, expected=expected,
        params={"action": action},
    )


def upload(client, token, encrypted_token, content: bytes, filename: str, expected):
    return client.post_multipart(
        f"upload/{encrypted_token}", token=token, expected=expected,
        files={"file": (filename, content, "application/octet-stream")},
    )


def test_file_document(client: APIClient, token: Optional[str], second_token: Optional[str],
                        created_ids: dict[str, list[int]]) -> tuple[TestSuite, list]:
    suite = TestSuite("FileDocument")
    observations: list = []
    print("\n[FileDocument — File Service happy path + governed negative tests]")

    # ── Happy path lifecycle (API-FILE-001 → 002 → 005 → issueAccessToken → 003 → 004) ──
    r = run(suite, "Issue Upload Token (API-FILE-001 happy path, TC-FILE-015)",
            issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201]))
    upload_token = get_field(r, "encryptedToken")

    r = run(suite, "Upload File within size limit (API-FILE-002, TC-FILE-001/016)",
            upload(client, token, upload_token, make_bytes(2 * 1024 * 1024), "invoice.pdf", expected=[200, 201]))
    file_pk = get_field(r, "fileDocumentPk")
    if file_pk:
        created_ids.setdefault("FileDocument", []).append(file_pk)

    if file_pk:
        run(suite, "List Files for Owner (API-FILE-005, TC-FILE-019)",
            client.get(f"api/v1/files/{OWNER_ID}", token=token, expected=[200]))

        r = run(suite, "Issue Download Access Token (issueAccessToken, action=DOWNLOAD)",
                issue_access_token(client, token, file_pk, "DOWNLOAD", expected=[200, 201]))
        dl_token = get_field(r, "encryptedToken")

        run(suite, "Download File (API-FILE-003 happy path, TC-FILE-004/017)",
            client.get(f"download/{dl_token}", token=token, expected=[200]))

        r = run(suite, "Issue Delete Access Token (issueAccessToken, action=DELETE)",
                issue_access_token(client, token, file_pk, "DELETE", expected=[200, 201]))
        del_token = get_field(r, "encryptedToken")

        run(suite, "Delete File — owner (API-FILE-004 happy path, TC-FILE-013/018)",
            client.delete(f"{del_token}", token=token, expected=[200]))

        # Edge case: download of a now-DELETED file → 410 (TC-FILE-020, ERR-FILE-0008)
        r = run(suite, "Issue Download Token for DELETED file",
                issue_access_token(client, token, file_pk, "DOWNLOAD", expected=[200, 201]))
        dl_token_2 = get_field(r, "encryptedToken")
        r = run(suite, "Download DELETED file — expect 410 (TC-FILE-020, ERR-FILE-0008)",
                client.get(f"download/{dl_token_2}", token=token, expected=[410]))
        code = error_code_of(r)
        if r.passed and code and code != "FILE_NO_LONGER_AVAILABLE":
            print(f"      ⚠ Status matched but error.code was '{code}', expected FILE_NO_LONGER_AVAILABLE")

    # ── RULE-FILE-001 / ERR-FILE-0001 — oversized upload rejected (TC-FILE-002/022) ──
    r = run(suite, "Issue Upload Token (for oversized-file test)",
            issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201]))
    ov_token = get_field(r, "encryptedToken")
    r = run(suite, "Upload File exceeding size limit — expect 400 (TC-FILE-002, ERR-FILE-0001)",
            upload(client, token, ov_token, make_bytes(6 * 1024 * 1024), "toolarge.bin", expected=[400]))
    code = error_code_of(r)
    if r.passed and code and code != "FILE_SIZE_EXCEEDED":
        print(f"      ⚠ Status matched but error.code was '{code}', expected FILE_SIZE_EXCEEDED")
    oversized_pk = get_field(r, "fileDocumentPk")
    if oversized_pk:
        created_ids.setdefault("FileDocument", []).append(oversized_pk)

    # ── Boundary: exactly at default 5MB ceiling succeeds (TC-FILE-003 pattern, ──
    # source: execution-plan.md documented DEFAULT_MAX_SIZE_BYTES = 5,242,880) ──
    r = run(suite, "Issue Upload Token (for boundary-size test)",
            issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201]))
    bd_token = get_field(r, "encryptedToken")
    r = run(suite, f"Upload File at exactly {DEFAULT_MAX_SIZE_BYTES} bytes (default ceiling boundary)",
            upload(client, token, bd_token, make_bytes(DEFAULT_MAX_SIZE_BYTES), "atlimit.bin", expected=[200, 201]))
    bd_pk = get_field(r, "fileDocumentPk")
    if bd_pk:
        created_ids.setdefault("FileDocument", []).append(bd_pk)

    # ── RULE-FILE-004 / ERR-FILE-0004 — reuse of consumed token rejected (TC-FILE-009/010) ──
    r = run(suite, "Issue Upload Token (for single-use test)",
            issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201]))
    su_token = get_field(r, "encryptedToken")
    r = run(suite, "First use of fresh token succeeds (TC-FILE-009)",
            upload(client, token, su_token, make_bytes(1024), "first.txt", expected=[200, 201]))
    su_pk = get_field(r, "fileDocumentPk")
    if su_pk:
        created_ids.setdefault("FileDocument", []).append(su_pk)
    r = run(suite, "Reuse of already-consumed token rejected (TC-FILE-010, ERR-FILE-0004)",
            upload(client, token, su_token, make_bytes(1024), "second.txt", expected=[401]))
    code = error_code_of(r)
    if r.passed and code and code != "FILE_TOKEN_ALREADY_USED":
        print(f"      ⚠ Status matched but error.code was '{code}', expected FILE_TOKEN_ALREADY_USED")

    # ── RULE-FILE-003 / ERR-FILE-0003 — tampered token (401) & action-mismatch (403) ──
    r = run(suite, "Issue Upload Token (for tamper test)",
            issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201]))
    raw_token = get_field(r, "encryptedToken")
    corrupted = tamper_token(raw_token) if raw_token else None
    run(suite, "Tampered token rejected (TC-FILE-008a, ERR-FILE-0003, expect 401)",
        upload(client, token, corrupted, make_bytes(1024), "tampered.txt", expected=[401]))

    if file_pk:
        # file_pk was already deleted above; re-issue a download token and replay it
        # against DELETE to force an action mismatch (upload token used earlier is gone,
        # so use a fresh download-action token against the delete endpoint instead).
        r = run(suite, "Issue Upload Token (for action-mismatch test)",
                issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201]))
        mismatch_token = get_field(r, "encryptedToken")
        run(suite, "Upload-action token replayed on DELETE — action mismatch (TC-FILE-008b, ERR-FILE-0003, expect 403)",
            client.delete(f"{mismatch_token}", token=token, expected=[403]))

    # ── RULE-FILE-007 / ERR-FILE-0007 — non-owner, non-Admin delete rejected (TC-FILE-014) ──
    if second_token:
        r = run(suite, "Issue Upload Token (for ownership test)",
                issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201]))
        own_token = get_field(r, "encryptedToken")
        r = run(suite, "Upload file as owner (setup for ownership test)",
                upload(client, token, own_token, make_bytes(1024), "owned.txt", expected=[200, 201]))
        own_pk = get_field(r, "fileDocumentPk")
        if own_pk:
            created_ids.setdefault("FileDocument", []).append(own_pk)
            r = run(suite, "Issue Delete Access Token (as owner, for cross-user attempt)",
                    issue_access_token(client, token, own_pk, "DELETE", expected=[200]))
            cross_del_token = get_field(r, "encryptedToken")
            r = run(suite, "Non-owner, non-Admin delete rejected (TC-FILE-014, ERR-FILE-0007, expect 403)",
                    client.delete(f"{cross_del_token}", token=second_token, expected=[403]))
            code = error_code_of(r)
            if r.passed and code and code != "FILE_DELETE_NOT_AUTHORIZED":
                print(f"      ⚠ Status matched but error.code was '{code}', expected FILE_DELETE_NOT_AUTHORIZED")
    else:
        print("  ⊘ Skipped — RULE-FILE-007 (TC-FILE-014) and TC-FILE-023 need SECOND_USER_* "
              "credentials (a non-owner, non-Admin account). Set them at the top of this "
              "script to enable these two scenarios.")

    # ── PLATFORM-STD / ERR-FILE-0009 — unknown fileCategoryFk (TC-FILE-021) ──
    r = run(suite, "Upload-token request with unknown fileCategoryFk (TC-FILE-021, ERR-FILE-0009, expect 404)",
            issue_upload_token(client, token, 999999999, expected=[404]))
    code = error_code_of(r)
    if r.passed and code and code != "FILE_CATEGORY_NOT_FOUND":
        print(f"      ⚠ Status matched but error.code was '{code}', expected FILE_CATEGORY_NOT_FOUND")

    # ── Edge case: empty list result → 200, not 404 (TC-FILE-024) ──
    run(suite, "List Files for owner with zero files — expect 200, empty content (TC-FILE-024)",
        client.get("api/v1/files/999888", token=token, expected=[200]))

    # ── TC-FILE-025 — SQL injection resistance via filename ──
    r = run(suite, "Issue Upload Token (for SQLi filename test)",
            issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201]))
    sqli_token = get_field(r, "encryptedToken")
    r = run(suite, "Upload with SQLi-shaped filename — expect 201, stored literally (TC-FILE-025)",
            upload(client, token, sqli_token, make_bytes(1024), "test' OR '1'='1.txt", expected=[200, 201]))
    sqli_pk = get_field(r, "fileDocumentPk")
    if sqli_pk:
        created_ids.setdefault("FileDocument", []).append(sqli_pk)
        stored_name = get_field(r, "fileNameOriginal")
        if stored_name and stored_name != "test' OR '1'='1.txt":
            print(f"      ⚠ fileNameOriginal stored as '{stored_name}', expected literal preservation")

    # ── RULE-FILE-002 / ERR-FILE-0002 — TTL expiry: NOT AUTOMATED (see header note #3) ──
    print("  ⊘ Skipped — RULE-FILE-002 (TC-FILE-005/006, token TTL=100min) requires a "
          "100+ minute wait or clock manipulation; not automated by this script.")

    # ── Stage E — exploratory scenarios (bounded, sourced, undocumented outcomes) ──
    # Source: issueUploadToken.md — ownerType Constraints: maxLength=100
    r = issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201, 400],
                            owner_type="X" * 100)
    run_observation(observations, "Issue Upload Token — ownerType at maxLength (100 chars)",
                     source="issueUploadToken.md ownerType Constraints: maxLength=100", result=r)

    r = issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201, 400],
                            owner_type="X" * 101)
    run_observation(observations, "Issue Upload Token — ownerType over maxLength (101 chars)",
                     source="issueUploadToken.md ownerType Constraints: maxLength=100", result=r)

    # Source: issueUploadToken.md — moduleCode Constraints: maxLength=20
    r = issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201, 400],
                            module_code="X" * 20)
    run_observation(observations, "Issue Upload Token — moduleCode at maxLength (20 chars)",
                     source="issueUploadToken.md moduleCode Constraints: maxLength=20", result=r)

    r = issue_upload_token(client, token, FILE_CATEGORY_FK, expected=[200, 201, 400],
                            module_code="X" * 21)
    run_observation(observations, "Issue Upload Token — moduleCode over maxLength (21 chars)",
                     source="issueUploadToken.md moduleCode Constraints: maxLength=20", result=r)

    # Source: issueUploadToken.md — required-field omissions (Required=Yes)
    for omitted_field in ("ownerId", "ownerType", "moduleCode", "fileCategoryFk"):
        payload = {"ownerId": OWNER_ID, "ownerType": OWNER_TYPE,
                   "moduleCode": MODULE_CODE, "fileCategoryFk": FILE_CATEGORY_FK}
        del payload[omitted_field]
        r = client.post("api/v1/files/upload-token", token=token, expected=[200, 201, 400],
                         json=payload)
        run_observation(observations, f"Issue Upload Token — omit required '{omitted_field}'",
                         source=f"issueUploadToken.md {omitted_field} Required=Yes", result=r)

    # Source: issueUploadToken.md — fileCategoryFk Type: integer (int64)
