# FileDocument has a real terminal DELETE, but — unlike a plain REST DELETE /{id} —
# it requires a freshly-issued, single-use access token (issueAccessToken, action=
# DELETE) rather than the id itself. This diverges from the generic boilerplate's
# id-based delete/deactivate pattern; adapted here rather than force-fit.
#
# FileCategory is never created by this script (DRV-FILE-001 — reference table,
# DB-seeded), so there is nothing to tear down for it.

def cleanup(client: APIClient, token: Optional[str], created_ids: dict[str, list[int]]) -> None:
    print("\n[Cleanup]")
    deleted, already_gone, failed = 0, 0, 0
    for file_pk in created_ids.get("FileDocument", []):
        if file_pk is None:
            continue
        r_tok = client.post(f"api/v1/files/{file_pk}/access-token", token=token,
                             expected=[200, 404, 410], params={"action": "DELETE"})
        del_token = get_field(r_tok, "encryptedToken")
        if not del_token:
            # Likely already deleted (404/410 on token issuance) — nothing further to do.
            already_gone += 1
            print(f"  ✓ FileDocument #{file_pk} — already gone or inaccessible, skipping")
            continue
        r_del = client.delete(f"{del_token}", token=token, expected=[200, 404, 410])
        if r_del.status_code == 200:
            deleted += 1
            print(f"  ✓ Deleted FileDocument #{file_pk}")
        elif r_del.status_code in (404, 410):
            already_gone += 1
            print(f"  ✓ FileDocument #{file_pk} — already deleted")
        else:
            failed += 1
            print(f"  ⚠ Could not delete FileDocument #{file_pk} (status {r_del.status_code})")

    print(f"  Summary: {deleted} deleted, {already_gone} already gone, {failed} failed")
    if failed:
        print("  ⚠ Some test records may remain in the database — review manually.")


# ─── Stage G — Problems Report (filtered, categorized) ─────────────────────────

def generate_problems_report(suites: list[TestSuite], output_path: str) -> int:
    likely_bugs, mismatches, infra = [], [], []
    for suite in suites:
        for r in suite.results:
            if r.passed:
                continue
            if r.error and ("Connection refused" in r.error or "timeout" in r.error.lower()):
                infra.append((suite.name, r))
            else:
                # Ambiguous failures default to "Likely Real Bug" per governance —
                # never silently classified as benign without a documented RULE/ERR
                # explaining why the observed status is actually correct.
                likely_bugs.append((suite.name, r))

    lines = ["# File Service — Problems Report", ""]
    lines.append(f"Generated {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    lines.append("")
    lines.append(f"🔴 Likely Real Bugs: {len(likely_bugs)}")
    lines.append(f"🟡 Test Assumption Mismatches: {len(mismatches)}")
    lines.append(f"⚪ Infrastructure: {len(infra)}")
    lines.append("")

    def section(title, items):
        out = [f"## {title}", ""]
        if not items:
            out.append("_None._\n")
            return out
        for suite_name, r in items:
            out.append(f"- **[{suite_name}] {r.name}**")
            out.append(f"  - `{r.method} {r.url}`")
            out.append(f"  - Got: `{r.status_code or 'ERR'}`, Expected: `{r.expected_statuses}`")
            if r.error:
                out.append(f"  - Error: {r.error}")
            out.append("")
        return out

    lines += section("🔴 Likely Real Bugs", likely_bugs)
    lines += section("🟡 Test Assumption Mismatches", mismatches)
    lines += section("⚪ Infrastructure", infra)

    Path(output_path).write_text("\n".join(lines), encoding="utf-8")
    print(f"📄 Problems report written to: {output_path}")
    return len(likely_bugs) + len(mismatches) + len(infra)


