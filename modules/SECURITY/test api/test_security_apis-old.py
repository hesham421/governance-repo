# ─── Entry point ────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="SECURITY module API test suite")
    parser.add_argument("--base-url", default=BASE_URL, help="Base API URL")
    parser.add_argument("--report", default="security_api_test_report.html", help="Output HTML file")
    parser.add_argument("--problems-report", default="security_problems_report.md", help="Output problems Markdown file")
    args = parser.parse_args()

    client = APIClient(args.base_url)
    start_time = time.perf_counter()

    print("=" * 60)
    print("  SECURITY Module API Test Suite")
    print(f"  Base URL: {args.base_url}")
    print(f"  Run ID:   {RUN_ID}")
    print("=" * 60)

    auth_suite, token = test_auth(client)
    admin_user_id = None  # not resolved from login response (login only returns tokens, not userId);
                          # loginWithToken's UserInfo.userId would resolve it, kept simple here.

    observations: list = []
    created_ids: dict[str, list[int]] = {"Page": [], "Permission": [], "Role": [], "User": []}

    menu_suite = TestSuite("Menu")
    page_suite = TestSuite("Page")
    permission_suite = TestSuite("Permission")
    role_suite = TestSuite("Role")
    user_suite = TestSuite("User")

    try:
        # Stage B dependency order: Page (root) -> Permission (optional FK
        # -> Page) -> Role (uses Page's pageCode for page-assignment
        # endpoints) -> User (uses Role's roleName for assignRoles).
        menu_suite = test_menu(client, token, admin_user_id)
        page_suite, page_ids = test_page(client, token, observations, created_ids)
        permission_suite, _perm_ids = test_permission(client, token, page_ids.get("id"), observations, created_ids)
        role_suite, role_ids = test_role(client, token, page_ids.get("pageCode"), observations, created_ids)
        user_suite, _user_ids = test_user(client, token, role_ids.get("roleName"), observations, created_ids)
    finally:
        # Always runs, even if a test above raised partway through.
        cleanup(client, token, created_ids)

    duration = time.perf_counter() - start_time
    suites = [auth_suite, menu_suite, page_suite, permission_suite, role_suite, user_suite]

    print("\n" + "=" * 60)
    print("  SUMMARY")
    print("=" * 60)
    total_pass = sum(s.passed for s in suites)
    total_all = sum(s.total for s in suites)
    for s in suites:
        bar = "█" * s.passed + "░" * s.failed
        print(f"  {s.name:<20} {s.passed:>3}/{s.total:<3}  {bar}")
    print(f"\n  Total: {total_pass}/{total_all} passed  ({duration:.1f}s)")
    if observations:
        print(f"  Exploratory observations: {len(observations)} (see HTML report — not pass/fail)")

    generate_html_report(suites, observations, args.report, duration, args.base_url)
    problems = classify_and_collect_problems(suites)
    generate_problems_report(problems, args.problems_report)

    sys.exit(0 if total_pass == total_all else 1)


if __name__ == "__main__":
    main()
