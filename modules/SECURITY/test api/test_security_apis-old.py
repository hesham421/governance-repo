# ─── Data classes (fixed pattern — do not change per module) ──────────────────

@dataclass
class TestResult:
    name: str
    method: str
    url: str
    status_code: Optional[int]
    passed: bool
    expected_statuses: list[int]
    duration_ms: float
    error: Optional[str] = None
    response_body: Optional[str] = None
    note: Optional[str] = None

    @property
    def status_label(self) -> str:
        return "PASS" if self.passed else "FAIL"


@dataclass
class TestSuite:
    name: str
    results: list[TestResult] = field(default_factory=list)

    @property
    def passed(self) -> int:
        return sum(1 for r in self.results if r.passed)

    @property
    def failed(self) -> int:
        return len(self.results) - self.passed

    @property
    def total(self) -> int:
        return len(self.results)


@dataclass
class Observation:
    """
    Stage E exploratory scenario with an UNDOCUMENTED expected outcome. Never
    counted as pass/fail — the point is to record actual behavior for human
    review, not to assert a guessed business rule.
    """
    name: str
    method: str
    url: str
    status_code: Optional[int]
    duration_ms: float
    source: str
    response_body: Optional[str] = None


@dataclass
class ProblemEntry:
    bucket: str  # "real_bug" | "assumption_mismatch" | "infrastructure"
    name: str
    detail: str
    result: "TestResult"


# ─── HTTP helper (fixed) ───────────────────────────────────────────────────────

class APIClient:
    def __init__(self, base_url: str):
        self.base_url = base_url.rstrip("/")
        self.session = requests.Session()
        self.session.headers.update({
            "Accept": "application/json",
            "Content-Type": "application/json",
        })

    def _make(self, method: str, path: str, token: Optional[str] = None,
              expected: Optional[list[int]] = None, **kwargs) -> TestResult:
        url = f"{self.base_url}/{path.lstrip('/')}"
        headers = kwargs.pop("headers", {})
        if token:
            headers["Authorization"] = f"Bearer {token}"
        if expected is None:
            expected = [200]

        start = time.perf_counter()
        try:
            resp = self.session.request(method.upper(), url, headers=headers, timeout=TIMEOUT, **kwargs)
            duration = (time.perf_counter() - start) * 1000
            try:
                body = json.dumps(resp.json(), indent=2, ensure_ascii=False)
            except Exception:
                body = resp.text[:500]
            return TestResult(
                name="", method=method.upper(), url=url, status_code=resp.status_code,
                passed=resp.status_code in expected, expected_statuses=expected,
                duration_ms=round(duration, 1), response_body=body,
            )
        except requests.exceptions.ConnectionError:
            duration = (time.perf_counter() - start) * 1000
            return TestResult(
                name="", method=method.upper(), url=url, status_code=None, passed=False,
                expected_statuses=expected, duration_ms=round(duration, 1),
                error="Connection refused — is the server running?",
            )
        except Exception as exc:
            duration = (time.perf_counter() - start) * 1000
            return TestResult(
                name="", method=method.upper(), url=url, status_code=None, passed=False,
                expected_statuses=expected, duration_ms=round(duration, 1), error=str(exc),
            )

    def get(self, path, token=None, expected=None, **kw):
        return self._make("GET", path, token, expected, **kw)

    def post(self, path, token=None, expected=None, **kw):
        return self._make("POST", path, token, expected, **kw)

    def put(self, path, token=None, expected=None, **kw):
        return self._make("PUT", path, token, expected, **kw)

    def delete(self, path, token=None, expected=None, **kw):
        return self._make("DELETE", path, token, expected, **kw)


# ─── Test runner helpers (fixed) ───────────────────────────────────────────────

def run(suite: TestSuite, name: str, result: TestResult, note: str = "") -> TestResult:
    result.name = name
    result.note = note
    suite.results.append(result)
    status = "✓" if result.passed else "✗"
    code = result.status_code or "ERR"
    print(f"  {status} [{code}] {name} ({result.duration_ms:.0f}ms)")
    if not result.passed and result.error:
        print(f"      Error: {result.error}")
    return result


def run_observation(bucket: list, name: str, source: str, result: TestResult) -> None:
    obs = Observation(
        name=name, method=result.method, url=result.url, status_code=result.status_code,
        duration_ms=result.duration_ms, source=source, response_body=result.response_body,
    )
    bucket.append(obs)
    code = result.status_code or "ERR"
    print(f"  👁 [{code}] {name} — observed (source: {source})")


def data_of(result: TestResult):
    """
    Envelope shape {success, message, data, error, timestamp, correlationId}
    confirmed against index.md's "Common Response Envelope" (Schema:
    ApiResponseUserDto).
    """
    if not result.response_body:
        return None
    try:
        return json.loads(result.response_body).get("data")
    except Exception:
        return None


def error_code_of(result: TestResult) -> Optional[str]:
    if not result.response_body:
        return None
    try:
        err = json.loads(result.response_body).get("error")
        return err.get("code") if isinstance(err, dict) else None
    except Exception:
        return None


def extract_token(result: TestResult) -> Optional[str]:
    d = data_of(result)
    return d.get("accessToken") if isinstance(d, dict) else None


def extract_id(result: TestResult) -> Optional[int]:
    d = data_of(result)
    return d.get("id") if isinstance(d, dict) else None


def extract_page_first_id(result: TestResult) -> Optional[int]:
    """
    Pagination envelope per index.md: totalElements, totalPages, size,
    number (NOT "page"), numberOfElements, first, last, empty, sort,
    pageable. `content` array is standard for Page<T> but not explicitly
    tabulated in index.md's Pagination Envelope section — treated as a
    documentation gap, not proof it's absent from the real response.
    """
    d = data_of(result)
    if isinstance(d, dict):
        content = d.get("content")  # unconfirmed in table — flagged, not asserted on
        if isinstance(content, list) and content:
            return content[0].get("id")
    return None


# ─── Auth (fixed pattern, extended: loginWithToken / refresh / logout) ────────
# Covers: API login, loginWithToken, refresh, logout
# (Authentication group — all 4/4 catalog endpoints)

def test_auth(client: APIClient) -> tuple[TestSuite, Optional[str]]:
    suite = TestSuite("Auth")
    print("\n[Auth]")
    r = run(suite, "Admin Login", client.post(
        "api/auth/login", expected=[200],
        json={"username": ADMIN_USERNAME, "password": ADMIN_PASSWORD},
    ))
    token = extract_token(r)

    # loginWithToken — separate call, doesn't affect `token` used for the rest
    # of the script; asserts the richer UserInfo shape (roles/permissions).
    run(suite, "Login With Token (full UserInfo)", client.post(
        "api/auth/login-token", expected=[200],
        json={"username": ADMIN_USERNAME, "password": ADMIN_PASSWORD},
    ))

    # refresh — relies on the refresh_token HttpOnly cookie set by the Login
    # call above (requests.Session retains cookies automatically).
    run(suite, "Refresh Access Token", client.post("api/auth/refresh", token=token, expected=[200]))

    # logout — revokes tokens via the same session cookie. Performed last in
    # this suite; does not invalidate the already-issued `token` string used
    # for Bearer auth in the rest of the script (stateless JWT, only the
    # refresh token/cookie is revoked — Rule 3/Rule 4/Rule 5 all describe
    # refresh-token rotation/revocation, not access-token blacklisting).
    run(suite, "Logout", client.post("api/auth/logout", token=token, expected=[204]))

    return suite, token


