# ─── Cleanup / Teardown (best-effort — see per-entity capability notes) ──────

# Reverse of the Stage B dependency/creation order: User -> Role -> Permission
# -> Page, so FK/business-rule dependencies (role must not be in use, etc.)
# don't block deactivation/deletion of a still-referenced parent.
ENTITY_TEARDOWN_ORDER = ["User", "Role", "Permission", "Page"]

ENTITY_PATHS = {
    "User": "api/users",
    "Role": "api/roles",
    "Permission": "api/permissions",
    "Page": "api/pages",
}

# Per-entity cleanup strategy, discovered from index.md's API Catalog:
#   hard_delete     -> a real DELETE /{id} endpoint exists
#   soft_deactivate -> only a .../deactivate endpoint exists (no DELETE)
#   none            -> neither exists; records cannot be removed via the API
ENTITY_CLEANUP_STRATEGY = {
    "User": "hard_delete",        # DELETE /api/users/{userId} (delete.md)
    "Role": "hard_delete",        # DELETE /api/roles/{roleId} (deleteRole.md)
    "Permission": "none",         # no delete/deactivate endpoint in the catalog at all
    "Page": "soft_deactivate",    # only PUT /api/pages/{id}/deactivate (deactivatePage.md)
}


def cleanup(client: APIClient, token: Optional[str], created_ids: dict[str, list[int]]) -> None:
    """
    Best-effort teardown, always executed (via try/finally in main()) even if
    a test raised partway through.
    """
    print("\n[Cleanup]")
    hard_deleted = 0
    soft_deactivated = 0
    no_cleanup_path = 0

    for entity in ENTITY_TEARDOWN_ORDER:
        ids = created_ids.get(entity, [])
        path = ENTITY_PATHS.get(entity)
        strategy = ENTITY_CLEANUP_STRATEGY.get(entity, "none")
        for _id in ids:
            if _id is None or path is None:
                continue
            if strategy == "hard_delete":
                r = client.delete(f"{path}/{_id}", token=token, expected=[200, 204, 404, 409])
                if r.status_code in (200, 204):
                    hard_deleted += 1
                    print(f"  ✓ Deleted {entity} #{_id}")
                else:
                    print(f"  ⚠ Could not delete {entity} #{_id} (status {r.status_code}) — may remain, possibly still blocked by a business rule")
            elif strategy == "soft_deactivate":
                r = client.put(f"{path}/{_id}/deactivate", token=token, expected=[200, 404, 409])
                if r.status_code == 200:
                    soft_deactivated += 1
                    print(f"  ✓ Deactivated {entity} #{_id} (no hard-delete endpoint documented)")
                else:
                    print(f"  ⚠ Could not deactivate {entity} #{_id} (status {r.status_code}) — may remain active")
            else:  # "none"
                no_cleanup_path += 1
                print(f"  ⚠ {entity} #{_id} has NO delete or deactivate endpoint in the API catalog — record will persist permanently")

    if soft_deactivated:
        print(f"  ⚠ {soft_deactivated} record(s) remain in the database (soft-deactivated) — no hard-delete endpoint documented for that entity.")
    if hard_deleted:
        print(f"  ✓ {hard_deleted} record(s) permanently removed.")
    if no_cleanup_path:
        print(f"  ⚠ {no_cleanup_path} record(s) (Permission) have no cleanup path at all via the documented API — this is a real, permanent side effect of running this script, not a bug in it.")


# ─── Problems Report (Stage G — filtered, failures-only) ─────────────────────

def classify_and_collect_problems(suites: list[TestSuite]) -> list[ProblemEntry]:
    """
    🔴 Likely Real Bugs: a call that should have been rejected succeeded
        instead, or failed with a status not explained by any documented
        rule. Ambiguous cases default here (visible/flagged), never to
        "benign" by default.
    🟡 Test Assumption Mismatches: backend correctly rejected, but with a
        different status than the documented rule stated for it (e.g. the
        error.code sanity-check prints above surfaced a mismatch) — the
        expected_statuses list itself was already satisfied, so these are
        only distinguishable from the error.code warnings printed inline
        during the run; this report focuses on outright status failures.
    ⚪ Infrastructure: connection/timeout failures.
    """
    problems: list[ProblemEntry] = []
    for suite in suites:
        for r in suite.results:
            if r.passed:
                continue
            if r.error and ("Connection refused" in r.error or "timeout" in r.error.lower()):
                problems.append(ProblemEntry("infrastructure", f"[{suite.name}] {r.name}",
                    f"{r.error}", r))
            else:
                # Any pass/fail failure not explained as infrastructure defaults
                # to "Likely Real Bug" per the mandatory ambiguous-case rule —
                # never silently reclassified as benign.
                problems.append(ProblemEntry("real_bug", f"[{suite.name}] {r.name}",
                    f"Expected status in {r.expected_statuses}, got {r.status_code}", r))
    return problems


def generate_problems_report(problems: list[ProblemEntry], output_path: str) -> None:
    real_bugs = [p for p in problems if p.bucket == "real_bug"]
    mismatches = [p for p in problems if p.bucket == "assumption_mismatch"]
    infra = [p for p in problems if p.bucket == "infrastructure"]

    lines = ["# SECURITY Module — Problems Report", "",
             f"Generated {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", "",
             "Only failures are listed here — see the HTML report for the full pass/fail detail.", ""]

    lines.append(f"## 🔴 Likely Real Bugs ({len(real_bugs)})")
    lines.append("")
    if real_bugs:
        for p in real_bugs:
            lines.append(f"- **{p.name}** — {p.detail}")
            if p.result.response_body:
                short = p.result.response_body[:300]
                lines.append(f"  ```\n  {short}\n  ```")
    else:
        lines.append("None.")
    lines.append("")

    lines.append(f"## 🟡 Test Assumption Mismatches ({len(mismatches)})")
    lines.append("")
    lines.append("None auto-classified into this bucket this run — see the console/error.code "
                  "warnings printed during Stage C tests above for any status/code mismatches "
                  "against registry-security.md's documented mapping; those did not fail their "
                  "`expected` status check but may still need the test's assumption corrected.")
    lines.append("")

    lines.append(f"## ⚪ Infrastructure ({len(infra)})")
    lines.append("")
    if infra:
        for p in infra:
            lines.append(f"- **{p.name}** — {p.detail}")
    else:
        lines.append("None.")
    lines.append("")

    Path(output_path).write_text("\n".join(lines), encoding="utf-8")
    print(f"📄 Problems report written to: {output_path}")


