# ─── Cleanup / Teardown (best-effort) ──────────────────────────────────────────

def cleanup(client: APIClient, token: Optional[str], created_ids: dict, problems: list) -> None:
    """
    Reverse of Stage B dependency order: RoleBranch (hard-delete, FK->Role) ->
    UserProfile (no delete endpoint exists — flagged, not removable) ->
    unassign roles from Users (pre-step, avoids deleteRole's 409) ->
    User (hard-delete, covers both admin-created and self-registration users)
    -> Role (hard-delete) -> Permission (no delete endpoint — flagged) ->
    Page (soft-deactivate only, no hard-delete endpoint).
    """
    print("\n[Cleanup]")
    hard_deleted = 0
    soft_deactivated = 0
    leaked_no_api_path = 0

    # 1. Role Branches — hard delete, composite key
    for (role_id, branch_id) in created_ids.get("RoleBranch", []):
        r = client.delete(f"api/v1/security/role-branches/{role_id}/{branch_id}", token=token,
                           expected=[200, 204, 404])
        if r.status_code in (200, 204):
            hard_deleted += 1
            print(f"  ✓ Deleted RoleBranch ({role_id},{branch_id})")
        else:
            print(f"  ⚠ Could not delete RoleBranch ({role_id},{branch_id}) (status {r.status_code})")

    # 2. User Profiles — NO delete endpoint exists (confirmed absent per
    # security-registry.md §2.8). Cannot deactivate either (update payload
    # has no isActiveFl field). This is a genuine cleanup gap in the API
    # itself, not a script limitation — surfaced explicitly, never silently
    # claimed as cleaned up.
    for _user_id in created_ids.get("UserProfile", []):
        leaked_no_api_path += 1
        print(f"  ⚠ UserProfile for user #{_user_id} has NO cleanup path via this API "
              f"(no DELETE endpoint, no isActiveFl in UpdateSecUserProfileRequest) — will remain in DB.")

    # 3. Pre-step: strip role assignments from any Users we created, so
    # deleteRole doesn't 409 on "role has user assignments".
    for _user_id in created_ids.get("User", []):
        client.put(f"api/users/{_user_id}/roles", token=token, expected=[200, 404], json={"roleNames": []})

    # 4. Users — hard delete (covers both admin-created and self-registered)
    for _user_id in created_ids.get("User", []):
        r = client.delete(f"api/users/{_user_id}", token=token, expected=[200, 204, 404, 409])
        if r.status_code in (200, 204):
            hard_deleted += 1
            print(f"  ✓ Deleted User #{_user_id}")
        elif r.status_code == 409:
            print(f"  ⚠ Could not delete User #{_user_id} — still has dependencies (status 409)")
        else:
            print(f"  ⚠ Could not delete User #{_user_id} (status {r.status_code})")

    # 5. Roles — hard delete
    for _role_id in created_ids.get("Role", []):
        r = client.delete(f"api/roles/{_role_id}", token=token, expected=[200, 204, 404, 409])
        if r.status_code in (200, 204):
            hard_deleted += 1
            print(f"  ✓ Deleted Role #{_role_id}")
        elif r.status_code == 409:
            print(f"  ⚠ Could not delete Role #{_role_id} — still has dependencies (status 409)")
        else:
            print(f"  ⚠ Could not delete Role #{_role_id} (status {r.status_code})")

    # 6. Permissions — NO delete endpoint exists anywhere in the API Catalog
    for _perm_id in created_ids.get("Permission", []):
        leaked_no_api_path += 1
        print(f"  ⚠ Permission #{_perm_id} has NO cleanup path via this API (no DELETE/deactivate endpoint) — will remain in DB.")

    # 7. Pages — soft-deactivate only (no hard-delete endpoint)
    for _page_id in created_ids.get("Page", []):
        r = client.put(f"api/pages/{_page_id}/deactivate", token=token, expected=[200, 404, 409])
        if r.status_code == 200:
            soft_deactivated += 1
            print(f"  ✓ Deactivated Page #{_page_id} (no hard-delete endpoint documented)")
        else:
            soft_deactivated += 1
            print(f"  ⚠ Could not deactivate Page #{_page_id} (status {r.status_code}) — may remain active")

    if soft_deactivated:
        print(f"  ⚠ {soft_deactivated} Page record(s) remain in the database (soft-deactivated) — "
              f"Page Management has no hard-delete endpoint per its API docs.")
    if leaked_no_api_path:
        print(f"  ⚠ {leaked_no_api_path} record(s) (UserProfile/Permission) have NO cleanup path via "
              f"this module's API at all — this is a documented API gap, not a script defect.")
        problems.append(ProblemEntry(
            "assumption", "Cleanup coverage gap",
            f"{leaked_no_api_path} UserProfile/Permission record(s) created by this run cannot be "
            f"removed or deactivated via any endpoint in this module's API Catalog.",
        ))
    if hard_deleted:
        print(f"  ✓ {hard_deleted} record(s) permanently removed.")


# ─── Problems Report (Stage G) ──────────────────────────────────────────────────

def generate_problems_report(problems: list, suites: list, output_path: str) -> None:
    bugs = [p for p in problems if p.bucket == "bug"]
    assumptions = [p for p in problems if p.bucket == "assumption"]
    infra = [p for p in problems if p.bucket == "infra"]

    # Infra failures: connection-refused style errors surfaced during the run
    for s in suites:
        for r in s.results:
            if not r.passed and r.error and "Connection refused" in r.error:
                infra.append(ProblemEntry("infra", r.name, r.error))

    lines = ["# Security Module — Problems Report", ""]
    lines.append("Filtered view: pass/fail failures only, categorized per Stage G. "
                  "Exploratory (Stage E) observations are NOT included here — see the HTML "
                  "report's 'Exploratory Observations' section instead, since their expected "
                  "outcome is undocumented by design.")
    lines.append("")
    lines.append(f"## 🔴 Likely Real Bugs ({len(bugs)})")
    lines.append("")
    if bugs:
        for p in bugs:
            lines.append(f"- **{p.name}** — {p.detail}")
    else:
        lines.append("_None._")
    lines.append("")
    lines.append(f"## 🟡 Test Assumption Mismatches ({len(assumptions)})")
    lines.append("")
    if assumptions:
        for p in assumptions:
            lines.append(f"- **{p.name}** — {p.detail}")
    else:
        lines.append("_None._")
    lines.append("")
    lines.append(f"## ⚪ Infrastructure ({len(infra)})")
    lines.append("")
    if infra:
        for p in infra:
            lines.append(f"- **{p.name}** — {p.detail}")
    else:
        lines.append("_None._")
    lines.append("")
    Path(output_path).write_text("\n".join(lines), encoding="utf-8")
    print(f"📄 Problems report written to: {output_path}")


