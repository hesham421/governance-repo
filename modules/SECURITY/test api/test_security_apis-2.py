# ─── Entry point ────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="Security module API test suite")
    parser.add_argument("--base-url", default=BASE_URL, help="Base API URL")
    parser.add_argument("--report", default="security_api_test_report.html", help="Output HTML file")
    parser.add_argument("--problems-report", default="security_problems_report.md", help="Output problems markdown file")
    args = parser.parse_args()

    client = APIClient(args.base_url)
    start_time = time.perf_counter()

    print("=" * 60)
    print("  Security Module API Test Suite")
    print("  ⚠ Conflict #19 override in effect for SEC-002 gap-package APIs")
    print(f"  Base URL: {args.base_url}")
    print("=" * 60)

    auth_suite, token = test_auth(client)
    observations: list = []
    problems: list = []
    created_ids: dict = {
        "Page": [], "Permission": [], "Role": [], "User": [],
        "UserProfile": [], "RoleBranch": [],
    }

    page_suite = TestSuite("Page Management")
    permission_suite = TestSuite("Permission Management")
    role_suite = TestSuite("Role Access Control")
    user_suite = TestSuite("User Management")
    menu_suite = TestSuite("Menu Management")
    user_profile_suite = TestSuite("Security DataScope - User Profiles")
    role_branch_suite = TestSuite("Security DataScope - Role Branches")
    self_service_suite = TestSuite("Self-Service Auth")

    try:
        # Stage B dependency order: root entities first, children threaded
        # with the parent's returned ID/code — never hardcoded.
        page_suite, page_ids = test_pages(client, token, observations, created_ids)
        permission_suite, _perm_ids = test_permissions(client, token, page_ids.get("id"), observations, created_ids)
        role_suite, role_ids = test_roles(client, token, page_ids.get("pageCode"), observations, created_ids)
        user_suite, user_ids = test_users(client, token, None, observations, created_ids)
        # role_name threaded in after Role creation completes, for assignRoles.
        # AssignRolesRequest.roleNames resolves via Role.roleName (RoleRepository.findByRoleName,
        # per UserService.assignRoles) — NOT roleCode, despite the field's plural-of-"name" look.
        if role_ids.get("id"):
            role_lookup = client.get(f"api/roles/{role_ids['id']}", token=token, expected=[200])
            role_name_val = extract_field(role_lookup, "roleName")
            if user_ids.get("id") and role_name_val:
                run(user_suite, "Assign Roles to User (post-hoc, role now available)", client.put(
                    f"api/users/{user_ids['id']}/roles", token=token, expected=[200],
                    json={"roleNames": [role_name_val]},
                ))
        menu_suite = test_menu(client, token, user_ids.get("id"))
        user_profile_suite = test_user_profiles(client, token, user_ids.get("id"), observations, created_ids, problems)
        role_branch_suite = test_role_branches(client, token, role_ids.get("id"), observations, created_ids, problems)
        self_service_suite = test_self_service_auth(client, observations, created_ids, problems)
    finally:
        cleanup(client, token, created_ids, problems)

    duration = time.perf_counter() - start_time
    suites = [auth_suite, page_suite, permission_suite, role_suite, user_suite,
              menu_suite, user_profile_suite, role_branch_suite, self_service_suite]

    print("\n" + "=" * 60)
    print("  SUMMARY")
    print("=" * 60)
    total_pass = sum(s.passed for s in suites)
    total_all = sum(s.total for s in suites)
    for s in suites:
        bar = "█" * s.passed + "░" * s.failed
        print(f"  {s.name:<38} {s.passed:>3}/{s.total:<3}  {bar}")
    print(f"\n  Total: {total_pass}/{total_all} passed  ({duration:.1f}s)")
    if observations:
        print(f"  Exploratory observations: {len(observations)} (see HTML report — not pass/fail)")
    if problems:
        print(f"  Problems flagged for review: {len(problems)} (see problems report)")

    generate_html_report(suites, observations, args.report, duration, args.base_url)
    generate_problems_report(problems, suites, args.problems_report)
    sys.exit(0 if total_pass == total_all else 1)


if __name__ == "__main__":
    main()
